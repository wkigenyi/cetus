    void resetEditor(){
        jtLocation.setText("");
        locationString = null;
        jtCode.setText("");
        locationCode = null;
        jtCountry.setText("");
        country = null;
      
        
    }

    @Override
    public void resultChanged(LookupEvent ev) {
        Lookup.Result<Countries> result = (Lookup.Result<Countries>)ev.getSource();
        
        for(Countries c : result.allInstances()){
            country = c;
            jtCountry.setText(country.getCountryName());
            modify();
        }
    }


    
    


