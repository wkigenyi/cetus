    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        
        entityManager.getTransaction().begin();    
        
        if(location == null){ //Add New Currency
            String insertSQL = "INSERT INTO [dbo].[Locations]\n" +
"           ([CountryID]\n" +
"           ,[LocationCode]\n" +
"           ,[LocationName]\n" +
"           ,[Deleted])\n" +
"     VALUES\n" +
"           (?,?,?,?)";
            Query query = entityManager.createNativeQuery(insertSQL);
            query.setParameter(1, country.getCountryID());
            query.setParameter(2, locationCode);
            query.setParameter(3, locationString);
            query.setParameter(4, false);
            
            
            
            
            query.executeUpdate();
            entityManager.getTransaction().commit(); 
            
            resetEditor();
            StatusDisplayer.getDefault().setStatusText("Location Added");
            String sqlString ="SELECT r from Locations r";
             Utility.loadLocations(sqlString);
        }else{
            
            Locations eba = entityManager.find(Locations.class, location.getLocationID());
            
            if(locationString != null){
                eba.setLocationName(locationString);
            }
            if(locationCode != null){
                eba.setLocationCode(locationCode);
            }
            if(country != null){
                
                eba.setCountryID(country);
            }
            
            
            
           
            
            
            
            entityManager.getTransaction().commit();
            String sqlString ="SELECT r from Locations r";
             Utility.loadLocations(sqlString);
            StatusDisplayer.getDefault().setStatusText("Location Saved");
            resetEditor();
        }
        

    }//GEN-LAST:event_jbSaveActionPerformed


